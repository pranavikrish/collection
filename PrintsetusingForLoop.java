import java.util.*;
public class PrintsetusingforLoop
{
public static void main(String  args[])
{

Set<String> hs=new HashSet<String>();
    hs.add("ram");
    hs.add("kishore");
    hs.add("shyam");
    hs.add("sai");
    hs.add("surekha");
    hs.add("priya");
 
for(String x:hs)
{
System.out.println(x);
}

}
}
